// create function that takes 2 parameters: base and height
function areaOfTriangle(base, height) {
  return (base * height) / 2;
}

// call the areaOfTriangle function to calculate the area of a triangle with base of 7 and height of 4
// store the value in the are variable
let area = areaOfTriangle(7, 4);

// print value stored in area
console.log("The area of triangle is " + area);
